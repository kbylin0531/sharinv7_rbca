function trim(str, leftTrim, rightTrim, mbspaceTrim) {
    if (leftTrim == null) {
        leftTrim = true;
    }

    if (rightTrim == null) {
        rightTrim = true;
    }

    if (mbspaceTrim == null) {
        mbspaceTrim = true;
    }

    var newstr = str;
    if (leftTrim) {
        newstr = newstr.replace(/^\s+/g, "");
        if (mbspaceTrim) {
            newstr = newstr.replace(/^(|\s)+/g, "");
        }
    }
    if (rightTrim) {
        newstr = newstr.replace(/\s+$/g, "");
        if (mbspaceTrim) {
            newstr = newstr.replace(/(|\s)+$/g, "");
        }
    }
    return newstr;
}
    